# Operadores artitmeticos
a = 30
b = 5

print(a + b)  # Suma
print(a - b)  # Resta
print(a * b)  # Multiplicacion
print(a / b)  # Division
print(a // b)  # Division entera
print(a % b)  # Modulo
print(a ** b)  # Potenciacion


# Operadores de comparacion
print(a == b)  # Igualdad
print(a != b)  # Diferencia
print(a < b)  # Menor que
print(a > b)  # Mayor que
print(a <= b)  # Menor o igual que
print(a >= b)  # Mayor o igual que


# Metodos
print(round(2.4))  # Retorna el entero mas cercano
print(abs(-2.9))   # Retorna el valor absoluto de un numero
